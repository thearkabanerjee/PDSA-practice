# include <iostream>
using namespace std;


int main(){
    long long n,m;
    cin >> n >> m;
    cout << n << " + " << m << " = "<< n+m << "\n\n";
    cout << n << " - " << m << " = "<< n-m << "\n\n";
    cout << n << " * " << m << " = "<< n*m << "\n\n";
    cout << n << " / " << m << " = "<< n/m << "\n\n";
    cout << n << " % " << m << " = "<< n%m << "\n";
    return 0;
}